<script type="text/javascript">
	$(document).ready(function(){
	
	$('#medicalstatus').change(function(){
		if ($('#medicalstatus').val() == 'Current Problem') {
			$('#since-group').show();
			$('#fromto-group').hide();
			$('#dateofsurgery-group').hide();
		} else if ($('#medicalstatus').val() == 'Past Problem') {
			$('#since-group').hide();
			$('#fromto-group').show();
			$('#dateofsurgery-group').hide();
		} else if ($('#medicalstatus').val() == 'Completed Surgery') {
			$('#since-group').hide();
			$('#fromto-group').hide();
			$('#dateofsurgery-group').show();
		} else {
			$('#since-group').hide();
			$('#fromto-group').hide();
			$('#dateofsurgery-group').hide();
		}
	});
	$('#medicalstatus').change();
		
	});
</script>